// Helper to convert real lat/lng to SVG map coordinates (1000x500 box)
export function latLngToXY(lat: number, lng: number): { x: number, y: number } {
  // Simple Mercator-like projection mapped to [0, 1000] and [0, 500]
  // Longitude: -180 to 180 mapped to 0 to 1000
  const x = ((lng + 180) / 360) * 1000;
  
  // Latitude: 85 to -60 mapped to 0 to 500
  // Squeeze and stretch latitude slightly to center the shipping lanes (Gulf to India and West Africa)
  const latMin = -60;
  const latMax = 85;
  const y = 500 - (((lat - latMin) / (latMax - latMin)) * 500);

  return { x: Math.round(x), y: Math.round(y) };
}
